// Extract PDF text in the browser via pdf.js (loaded on demand).
async function extractPdfText(file) {
  const pdfjsLib = await loadPdfJs();
  const buf = await file.arrayBuffer();
  const pdf = await pdfjsLib.getDocument({ data: buf }).promise;
  let text = "";
  for (let i = 1; i <= pdf.numPages; i++) {
    const page = await pdf.getPage(i);
    const content = await page.getTextContent();
    text += content.items.map((it) => it.str).join(" ") + "\n";
  }
  return text.trim();
}

function OfferInput({ onDecoded, label = "Paste your offer letter here", cta = "Decode" }) {
  const [text, setText] = useS("");
  const [loading, setLoading] = useS(false);
  const [stage, setStage] = useS(null); // "parsing" | "brief"
  const [error, setError] = useS(null);
  const [fileName, setFileName] = useS(null);

  const decode = async (offerText) => {
    if (!offerText.trim() || loading) return;
    setLoading(true);
    setError(null);
    try {
      setStage("parsing");
      const response = await fetch("/api/parse-offer", {
        method: "POST",
        headers: { "content-type": "application/json" },
        body: JSON.stringify({ offerText }),
      });
      const payload = await response.json();
      if (!response.ok) throw new Error(payload.error || "Failed to parse offer");
      applyParsedOffer(payload.offer);

      // Second call: company/location context brief (slower — it's a Claude call).
      setStage("brief");
      try {
        const briefRes = await fetch("/api/offer-brief", {
          method: "POST",
          headers: { "content-type": "application/json" },
          body: JSON.stringify({ offer: OFFER }),
        });
        const briefPayload = await briefRes.json();
        if (briefRes.ok && briefPayload.brief) applyBrief(briefPayload.brief);
      } catch (briefErr) {
        // Brief is best-effort — numeric sections still render with defaults.
        console.warn("offer-brief failed:", briefErr);
      }

      recomputeY1();
      onDecoded();
    } catch (e) {
      setError(e && e.message ? e.message : String(e));
    } finally {
      setLoading(false);
      setStage(null);
    }
  };

  const handleFile = async (e) => {
    const file = e.target.files && e.target.files[0];
    if (!file) return;
    setFileName(file.name);
    setError(null);
    try {
      if (file.type === "application/pdf" || file.name.toLowerCase().endsWith(".pdf")) {
        setLoading(true);
        setStage("parsing");
        const extracted = await extractPdfText(file);
        setText(extracted);
        setLoading(false);
        setStage(null);
        if (extracted) decode(extracted);
      } else {
        const raw = await file.text();
        setText(raw);
        if (raw) decode(raw);
      }
    } catch (err) {
      setLoading(false);
      setStage(null);
      setError("Couldn't read that file: " + (err && err.message ? err.message : String(err)));
    }
  };

  const stageLabel = stage === "brief" ? "Researching company & location…" : "Reading your offer…";

  return (
    <section className="offer-input">
      <label className="offer-input-label" htmlFor="offer-text">
        {label}
      </label>
      <textarea
        id="offer-text"
        className="offer-input-textarea"
        value={text}
        onChange={(e) => setText(e.target.value)}
        placeholder="Paste the full text of your offer letter, or upload a PDF below…"
        rows={6}
        disabled={loading}
      />
      <div className="offer-input-row">
        <button
          className="offer-input-btn"
          onClick={() => decode(text)}
          disabled={loading || !text.trim()}
        >
          {loading ? (
            <>
              <span className="offer-input-spinner" aria-hidden="true" />
              {stageLabel}
            </>
          ) : (
            cta
          )}
        </button>
        <label className="offer-input-upload">
          <input type="file" accept="application/pdf,.pdf,.txt" onChange={handleFile} disabled={loading} hidden />
          <span>⬆ Upload PDF</span>
        </label>
        {fileName && !loading && <span className="offer-input-file">{fileName}</span>}
        {error && <div className="offer-input-error">⚠ {error}</div>}
      </div>
    </section>
  );
}

