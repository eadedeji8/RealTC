// Metrics for one offer/brief pair, used by the compare table.
function offerMetrics(offer, brief) {
  const th = computeTakeHome(offer, brief);
  const perksValue = (brief.benefits || []).reduce(
    (s, b) => s + (typeof b.estAnnualValueUSD === "number" ? b.estAnnualValueUSD : 0), 0);
  const coli = brief.location && typeof brief.location.costOfLivingIndex === "number"
    ? brief.location.costOfLivingIndex : 100;
  return {
    location: offer.location,
    grossY1: th.gross,
    takeHome: th.takeHome,
    realTakeHome: Math.round(th.takeHome / (coli / 100)), // cost-of-living adjusted
    rsuTotal: offer.rsuTotal,
    perksValue,
    layoffRounds: (brief.layoffs || []).length,
    coli,
  };
}

function CompareScreen({ onBack }) {
  const [text, setText] = useS("");
  const [loading, setLoading] = useS(false);
  const [stage, setStage] = useS(null);
  const [error, setError] = useS(null);
  const [offerB, setOfferB] = useS(null);
  const [briefB, setBriefB] = useS(null);

  const decode = async (offerText) => {
    if (!offerText.trim() || loading) return;
    setLoading(true); setError(null);
    try {
      setStage("parsing");
      const r = await fetch("/api/parse-offer", {
        method: "POST", headers: { "content-type": "application/json" },
        body: JSON.stringify({ offerText }),
      });
      const p = await r.json();
      if (!r.ok) throw new Error(p.error || "Failed to parse offer");
      const parsed = p.offer;
      const ob = {
        company: parsed.company || "Offer B",
        role: parsed.role || OFFER.role,
        location: parsed.location || "—",
        base: parsed.baseSalary ?? 0,
        signOn: parsed.signOnBonus ?? 0,
        rsuTotal: parsed.rsuTotal ?? 0,
        rsuYears: parsed.rsuVestingYears ?? 4,
        bonusPct: parsed.annualBonusPercent ?? 0,
      };
      setStage("brief");
      let bb = { location: {}, benefits: [], layoffs: [], isPublic: true };
      try {
        const br = await fetch("/api/offer-brief", {
          method: "POST", headers: { "content-type": "application/json" },
          body: JSON.stringify({ offer: ob }),
        });
        const bp = await br.json();
        if (br.ok && bp.brief) bb = bp.brief;
      } catch (e) { console.warn("brief B failed", e); }
      setOfferB(ob);
      setBriefB(bb);
    } catch (e) {
      setError(e && e.message ? e.message : String(e));
    } finally {
      setLoading(false); setStage(null);
    }
  };

  const handleFile = async (e) => {
    const file = e.target.files && e.target.files[0];
    if (!file) return;
    try {
      const isPdf = file.type === "application/pdf" || file.name.toLowerCase().endsWith(".pdf");
      const raw = isPdf ? await extractPdfText(file) : await file.text();
      setText(raw);
      if (raw) decode(raw);
    } catch (err) {
      setError("Couldn't read that file: " + (err && err.message ? err.message : String(err)));
    }
  };

  const a = offerMetrics(OFFER, BRIEF);
  const b = offerB ? offerMetrics(offerB, briefB) : null;

  const rows = [
    { label: "Location", a: a.location, b: b && b.location, fmt: (v) => v },
    { label: "Gross year-1 comp", a: a.grossY1, b: b && b.grossY1, fmt: fmtMoney, hi: "max" },
    { label: "Take-home (after tax + rent)", a: a.takeHome, b: b && b.takeHome, fmt: fmtMoney, hi: "max" },
    { label: "Cost-of-living adjusted", a: a.realTakeHome, b: b && b.realTakeHome, fmt: fmtMoney, hi: "max" },
    { label: "RSU grant (4-yr)", a: a.rsuTotal, b: b && b.rsuTotal, fmt: fmtMoney, hi: "max" },
    { label: "Perks value / yr", a: a.perksValue, b: b && b.perksValue, fmt: fmtMoney, hi: "max" },
    { label: "Layoff rounds (recent)", a: a.layoffRounds, b: b && b.layoffRounds, fmt: (v) => v, hi: "min" },
  ];

  return (
    <ScreenShell
      eyebrow="Compare offers"
      title="Two offers, side by side."
      lede="The headline number rarely wins. Compare what actually lands in your account, adjusted for where you'd live."
      onBack={onBack}
    >
      {!offerB && (
        <section className="offer-input">
          <label className="offer-input-label">Paste your second offer letter</label>
          <textarea className="offer-input-textarea" value={text}
            onChange={(e) => setText(e.target.value)} rows={6} disabled={loading}
            placeholder="Paste the full text of the other offer, or upload a PDF…" />
          <div className="offer-input-row">
            <button className="offer-input-btn" onClick={() => decode(text)} disabled={loading || !text.trim()}>
              {loading ? <><span className="offer-input-spinner" aria-hidden="true" />
                {stage === "brief" ? "Researching…" : "Reading…"}</> : "Compare"}
            </button>
            <label className="offer-input-upload">
              <input type="file" accept="application/pdf,.pdf,.txt" onChange={handleFile} disabled={loading} hidden />
              <span>⬆ Upload PDF</span>
            </label>
            {error && <div className="offer-input-error">⚠ {error}</div>}
          </div>
        </section>
      )}

      {b && (
        <section className="eq-section">
          <div className="cmp-table">
            <div className="cmp-row cmp-head">
              <div className="cmp-metric"></div>
              <div className="cmp-cell">{OFFER.company}</div>
              <div className="cmp-cell">{offerB.company}</div>
            </div>
            {rows.map((row, i) => {
              const aWins = row.hi && typeof row.a === "number" && typeof row.b === "number" &&
                (row.hi === "max" ? row.a > row.b : row.a < row.b);
              const bWins = row.hi && typeof row.a === "number" && typeof row.b === "number" &&
                (row.hi === "max" ? row.b > row.a : row.b < row.a);
              return (
                <div key={i} className="cmp-row">
                  <div className="cmp-metric">{row.label}</div>
                  <div className={"cmp-cell" + (aWins ? " cmp-win" : "")}>{row.fmt(row.a)}</div>
                  <div className={"cmp-cell" + (bWins ? " cmp-win" : "")}>{row.fmt(row.b)}</div>
                </div>
              );
            })}
          </div>
          <p className="fine" style={{ marginTop: 16 }}>
            Highlighted cells are the stronger value on each row. &ldquo;Cost-of-living adjusted&rdquo; divides
            take-home by the local cost-of-living index &mdash; $1 goes further in some cities than others.
            All figures are AI-generated estimates.
          </p>
          <button className="offer-input-btn" style={{ marginTop: 16 }}
                  onClick={() => { setOfferB(null); setBriefB(null); setText(""); }}>
            Compare a different offer
          </button>
        </section>
      )}
    </ScreenShell>
  );
}

